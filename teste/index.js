let nome = "Superman"
let xp = 1001
if(xp < 1000)
    console.log(nome, "Ferro")
    else if(xp > 1000 && xp <= 2000)
        console.log(nome, "Bronze")
        else if(xp > 2000 && xp <= 5000)
            console.log(nome, "Prata")
        else if(xp > 5000 && xp <= 7000)
            console.log(nome, "Ouro")
        else if(xp > 7000 && xp <= 8000)
            console.log(nome, "Platina")
        else if(xp > 8000 && xp <= 9000)
            console.log(nome, "Ascendente")
        else if(xp > 9000 && xp <= 10000)
            console.log(nome, "Imortal")
        else
            console.log(nome, "Radiante")